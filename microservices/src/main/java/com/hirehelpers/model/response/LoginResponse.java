package com.hirehelpers.model.response;

public class LoginResponse {
	
	private String status;
	
	private int id;
	
	public LoginResponse() {
		super();
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
